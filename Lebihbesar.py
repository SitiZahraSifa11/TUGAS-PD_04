# Tuliskan program yang meminta input dua bilangan dan menampilkan bilangan yang lebih besar.

angka_a =int(input("Masukkan angka pertama :"))
angka_b = int(input("Masukkan angka kedua :"))

if angka_a > angka_b :
    print("Bilangan pertama lebih besar:", angka_a)
elif angka_a<angka_b :
    print ("Bilangan kedua lebih besar :",angka_b)
else:
    print("Bilangan pertama dan kedua sama besar:", angka_a)



